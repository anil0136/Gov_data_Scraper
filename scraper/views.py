def home(request):
    sections = [
        _section(config["title"], kind)
        for kind, config in DASHBOARD_SOURCES.items()
    ]

    stats = {
        "sources": len(sections),
        "total_saved": sum(section["total_count"] for section in sections),
        "total_shown": sum(section["clean_count"] for section in sections),
        "total_hidden": sum(section["hidden_count"] for section in sections),
    }

    return render(request, "scraper/home.htm", {"sections": sections, "stats": stats})


def run_all(request):
    started = start_scraper_once()
    message = (
        "Scraping Started!"
        if started
        else "Scraping is already running or automatic scraping is disabled."
    )
    return render(request, "scraper/run_all.htm", {"message": message})
